const height = 173;

const idealWeight = (height - 100) * 9 / 10; // Tính Cân nặng lý tưởng
const maxWeight = height - 100; // Tính Cân nặng tối đa
const minWeight = (height - 100) * 8 / 10; // Cân nặng tối thiểu

console.log('Cân nặng lý tưởng của bạn là:', idealWeight, 'kg. Cân nặng tối đa là:', maxWeight, 
    'kg. Cân nặng tối thiểu là:', minWeight, 'kg.');